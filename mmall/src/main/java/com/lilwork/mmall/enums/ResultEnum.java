package com.lilwork.mmall.enums;

public enum ResultEnum {
    STOCK_ERROR(1,"Out of stock");

    private Integer code;
    private String msg;

    ResultEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
